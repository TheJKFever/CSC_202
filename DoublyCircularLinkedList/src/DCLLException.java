
public class DCLLException extends Exception {


	public DCLLException() {
		super();
	}

	public DCLLException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public DCLLException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}


	public DCLLException(String message) {
		super(message);
	}


	public DCLLException(Throwable arg0) {
		super(arg0);
	}
	
}
